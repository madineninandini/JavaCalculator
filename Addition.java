package corejava;
class Addition {
    static class Addition1 {
        int add(int x, int y) {
            return x + y;
        }
    }

    static class Substraction {
        int sub(int x, int y) {
            return x - y;
        }
    }

    static class Multiplication {
        int mul(int x, int y) {
            return x * y;
        }
    }

    static class Division {
        int div(int x, int y) {
            return x / y;
        }
    }

    static class ModDivision {
        int moddiv(int x, int y) {
            return x % y;
        }
    }
}